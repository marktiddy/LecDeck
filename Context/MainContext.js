//The component
export const MainContext = React.createContext();

export const MainProvider = ({ children }) => {
  const [appData, setAppData] = useState(data_to_use.data);
  const [error, setError] = useState(null);
  const [isLoading, setIsLoading] = useState(true);
  const [currentSundayIndex, setCurrentSundayIndex] = useState(3000);
  const [lectionary, setLectionary] = useState(dates_to_use);

  //Helper function to get this Sunday
  const getThisSunday = () => {
    const dayToGet = 7; //Sunday
    let today = new Date();
    const diff = today.getDay() - dayToGet;
    if (diff > 0) {
      today.setDate(today.getDate() + 6);
      return today;
    } else if (diff < 0) {
      today.setDate(today.getDate() + -1 * diff);
      return today;
    }
  };

  useEffect(() => {
    //New function to compare dates
    if (currentSundayIndex === 3000) {
      for (var i = 0; i < dates_to_use.dates.length; i++) {
        const arrDate = moment(dates_to_use.dates[i])
          .utc()
          .local()
          .format('ddd Do MMM YY');
        const thisSunday = moment(getThisSunday())
          .utc()
          .local()
          .format('ddd Do MMM YY');
        if (thisSunday === arrDate) {
          setCurrentSundayIndex(i);
          setIsLoading(false);
          break;
        }
      }
    }
  }, []);

  return (
    <MainContext.Provider
      value={{ appData, lectionary, currentSundayIndex, error, isLoading }}
    >
      {children}
    </MainContext.Provider>
  );
};

